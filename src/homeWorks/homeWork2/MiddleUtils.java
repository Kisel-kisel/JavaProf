package homeWorks.homeWork2;

public class MiddleUtils {
    public static double middleOfTwo(double first, double second){
        double middle = (first + second) / 2;
        return middle;
    }
    public static double middleOfThree(double first, double second, double third){
        double middle = (first + second + third) / 3;
        return middle;
    }
}
